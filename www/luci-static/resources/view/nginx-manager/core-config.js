function parseDirectiveName(line) {
	var m = /^\s*([A-Za-z0-9_]+)(?:\s|;|$)/.exec(line || '');
	return m ? m[1] : '';
}

function parseSharedZone(value) {
	var m = /\bshared:([^:\s;]+):([^;\s]+)/.exec(value || '');
	return m ? { name: m[1], size: m[2] } : null;
}

return view.extend({
	load: function() {
		return callSyncCoreFromNginx();
	},

	formValue: function(option, fallback) {
		if (option && typeof option.formvalue === 'function') {
			var value = option.formvalue('global');
			if (value != null)
				return String(value);
		}
		return fallback || '';
	},

	getCustomSslConflicts: function() {
		var options = this.customSslOptions || {};
		var custom = this.formValue(options.customSslDirectives, '');
		var conflicts = [];

		if (!custom)
			return conflicts;

		var sslSessionCache = this.formValue(options.sslSessionCache, '') || 'shared:SSL:10m';
		var sslStapling = this.formValue(options.sslStapling, '1');
		var sslBufferSize = this.formValue(options.sslBufferSize, '');
		var securityHeaders = this.formValue(options.securityHeaders, '1');
		var managedCache = parseSharedZone(sslSessionCache);
		var managedSecurityHeaders = {
			'X-Frame-Options': true,
			'Content-Security-Policy': true,
			'X-Content-Type-Options': true,
			'Referrer-Policy': true,
			'Permissions-Policy': true
		};
		var managedServerDirectives = {
			ssl_certificate: _('certificate path generated by site settings'),
			ssl_certificate_key: _('certificate key path generated by site settings'),
			ssl_protocols: _('SSL protocol settings generated by site settings'),
			ssl_ciphers: _('SSL cipher settings generated by site settings'),
			ssl_prefer_server_ciphers: _('SSL cipher preference generated by Nginx Manager'),
			ssl_session_timeout: _('SSL Session Timeout field'),
			ssl_session_tickets: _('SSL Session Tickets field'),
			ssl_trusted_certificate: _('OCSP trusted certificate generated by Nginx Manager'),
			http2: _('HTTP/2 field')
		};

		if (sslBufferSize)
			managedServerDirectives.ssl_buffer_size = _('SSL Buffer Size field');

		if (sslStapling === '1') {
			managedServerDirectives.ssl_stapling = _('OCSP Stapling field');
			managedServerDirectives.ssl_stapling_verify = _('OCSP Stapling field');
			managedServerDirectives.resolver = _('OCSP Stapling generated resolver');
			managedServerDirectives.resolver_timeout = _('OCSP Stapling generated resolver timeout');
		}

		custom.split(/\r?\n/).forEach(function(line, idx) {
			var trimmed = line.trim();
			if (!trimmed || trimmed.charAt(0) === '#')
				return;

			var directive = parseDirectiveName(line);
			if (!directive)
				return;

			if (directive === 'ssl_session_cache') {
				var customCache = parseSharedZone(line);
				if (customCache && managedCache && customCache.name === managedCache.name && customCache.size !== managedCache.size) {
					conflicts.push(_('Line') + ' ' + (idx + 1) + ': ssl_session_cache ' +
						_('reuses shared zone') + ' "' + customCache.name + '" ' +
						_('with size') + ' ' + customCache.size + ', ' +
						_('but SSL Session Cache uses') + ' ' + sslSessionCache + '. ' +
						_('Use a different zone name or change the SSL Session Cache field.'));
				}
				return;
			}

			if (directive === 'add_header') {
				var m = /^\s*add_header\s+(\S+)/.exec(line);
				var headerName = m ? m[1] : '';
				if (headerName === 'Strict-Transport-Security') {
					conflicts.push(_('Line') + ' ' + (idx + 1) + ': "add_header Strict-Transport-Security" ' +
						_('is managed by HSTS settings. Set HSTS Max Age to 0 to disable it.'));
				}
				else if (headerName === 'Alt-Svc') {
					conflicts.push(_('Line') + ' ' + (idx + 1) + ': "add_header Alt-Svc" ' +
						_('is managed by HTTP/3 settings. Disable HTTP/3 to customize it.'));
				}
				else if (securityHeaders === '1' && managedSecurityHeaders[headerName]) {
					conflicts.push(_('Line') + ' ' + (idx + 1) + ': "add_header ' + headerName + '" ' +
						_('is managed by Security Headers. Disable Security Headers to customize it.'));
				}
				return;
			}

			if (managedServerDirectives[directive]) {
				conflicts.push(_('Line') + ' ' + (idx + 1) + ': "' + directive + '" ' +
					_('is already managed by') + ' ' + managedServerDirectives[directive] + '. ' +
					_('Remove this line from Custom SSL Directives and use the dedicated field instead.'));
			}
		});

		return conflicts;
	},

	showCustomSslConflictModal: function(conflicts) {
		ui.showModal(_('Custom SSL Directives Conflict'), [
			E('p', {}, _('Please adjust Custom SSL Directives before saving. These lines conflict with settings generated by Nginx Manager:')),
			E('ul', {}, conflicts.map(function(item) {
				return E('li', {}, item);
			})),
			E('div', { 'class': 'right' }, [
				E('button', { 'type': 'button', 'class': 'btn', 'click': function() { ui.hideModal(); } }, _('Close'))
			])
		]);
	},

	validateBeforeSave: function() {
		var conflicts = this.getCustomSslConflicts();
		if (conflicts.length) {
			this.showCustomSslConflictModal(conflicts);
			return false;
		}
		return true;
	},

	render: function() {
		utils.loadSharedCSS();

		var m = new form.Map('nginx_manager', _('Core Config'));
		this.map = m;
		this.customSslOptions = {};

		/* ========== General Settings ========== */
		var s1 = m.section(form.TypedSection, 'global', _('General Settings'));
		s1.anonymous = true;

		var o;

		o = s1.option(form.Value, 'client_max_body_size', _('Client Max Body Size'));
		o.placeholder = '1m';
		o.rmempty = true;

		o = s1.option(form.Value, 'keepalive_timeout', _('Keepalive Timeout'));
		o.placeholder = '65';
		o.datatype = 'uinteger';
		o.rmempty = true;

		o = s1.option(form.Flag, 'gzip', _('Gzip'));
		o.rmempty = false;

		o = s1.option(form.ListValue, 'server_tokens', _('Server Tokens'));
		o.value('', _('Default'));
		o.value('off', _('Off'));
		o.value('on', _('On'));
		o.rmempty = true;

		o = s1.option(form.Flag, 'sendfile', _('Sendfile'));
		o.rmempty = false;

		o = s1.option(form.Flag, 'tcp_nopush', _('TCP NOPUSH'),
			_('Optimizes sending large files. Works best with Sendfile enabled.'));
		o.rmempty = false;
		o.default = '1';

		o = s1.option(form.Flag, 'tcp_nodelay', _('TCP NODELAY'),
			_('Disables Nagle algorithm for low-latency delivery. Useful for real-time applications.'));
		o.rmempty = false;

		o = s1.option(form.Value, 'access_log', _('Access Log'));
		o.placeholder = '/var/log/nginx/access.log';
		o.rmempty = true;

		o = s1.option(form.Value, 'error_log', _('Error Log'));
		o.placeholder = '/var/log/nginx/error.log';
		o.rmempty = true;

		/* ========== System Behavior ========== */
		var s3 = m.section(form.TypedSection, 'global', _('System Behavior'));
		s3.anonymous = true;

		o = s3.option(form.Flag, 'auto_backup', _('Auto Backup'),
			_('Automatically create a backup before applying configuration changes.'));
		o.rmempty = false;
		o.default = '1';

		o = s3.option(form.Value, 'max_backups', _('Max Backups'),
			_('Maximum number of backups to keep. Older backups will be automatically deleted.'));
		o.datatype = 'uinteger';
		o.rmempty = true;
		o.placeholder = '10';

		o = s3.option(form.Flag, 'test_before_reload', _('Test Before Reload'),
			_('Run nginx -t to test configuration before reloading. Prevents invalid config from breaking the server.'));
		o.rmempty = false;
		o.default = '1';

		o = s3.option(form.Value, 'managed_dir', _('Managed Config Directory'),
			_('Directory for managed site configuration files.'));
		o.rmempty = true;
		o.placeholder = '/etc/nginx/conf.d/luci-manager';

		o = s3.option(form.Value, 'backup_dir', _('Backup Directory'),
			_('Directory for storing configuration backups.'));
		o.rmempty = true;
		o.placeholder = '/etc/nginx-manager/backups';

	/* ========== SSL Settings ========== */
		var s2 = m.section(form.TypedSection, 'global', _('SSL Settings'));
		s2.anonymous = true;

		o = s2.option(form.Value, 'ssl_session_timeout', _('SSL Session Timeout'),
			_('Duration for SSL session cache, e.g. 1d, 4h, 30m.'));
		o.placeholder = '1d';
		o.rmempty = true;

		o = s2.option(form.Value, 'ssl_session_cache', _('SSL Session Cache'),
			_('Shared memory zone for SSL sessions, e.g. shared:SSL:10m. Leave empty for default.'));
		o.placeholder = 'shared:SSL:10m';
		o.rmempty = true;
		this.customSslOptions.sslSessionCache = o;

		o = s2.option(form.Flag, 'ssl_session_tickets', _('SSL Session Tickets'),
			_('Enable to improve TLS resumption performance, or disable for stricter forward secrecy.'));
		o.rmempty = true;
		o.default = '1';

		o = s2.option(form.Flag, 'ssl_stapling', _('OCSP Stapling'),
			_('Enable OCSP stapling for managed ACME certificates. Other certificate types keep stapling off to avoid issuer-chain or missing-OCSP warnings.'));
		o.rmempty = true;
		o.default = '1';
		this.customSslOptions.sslStapling = o;

		o = s2.option(form.Flag, 'http2', _('HTTP/2'),
			_('Enable HTTP/2 for SSL sites. Uses the http2 directive (nginx 1.25.1+).'));
		o.rmempty = true;
		o.default = '1';

		o = s2.option(form.Flag, 'http3', _('HTTP/3 (QUIC)'),
			_('Enable HTTP/3 over QUIC. Adds quic reuseport to listen directives and Alt-Svc header. Requires nginx built with QUIC support.'));
		o.rmempty = true;

		o = s2.option(form.Flag, 'security_headers', _('Security Headers'),
			_('Add X-Frame-Options, CSP, X-Content-Type-Options, Referrer-Policy, Permissions-Policy to SSL sites.'));
		o.rmempty = true;
		o.default = '1';
		this.customSslOptions.securityHeaders = o;

		o = s2.option(form.Value, 'ssl_buffer_size', _('SSL Buffer Size'),
			_('Send buffer size for SSL data, e.g. 4k, 8k, 16k. Leave empty for default.'));
		o.placeholder = '16k';
		o.rmempty = true;
		this.customSslOptions.sslBufferSize = o;

		o = s2.option(form.TextValue, 'custom_ssl_directives', _('Custom SSL Directives'),
			_('One directive per line. These will be added inside the server block.'));
		o.rmempty = true;
		o.rows = 4;
		o.placeholder = 'ssl_dhparam /etc/nginx/dhparam.pem;\nssl_ecdh_curve X25519:P-256:P-384;';
		this.customSslOptions.customSslDirectives = o;

		o = s2.option(form.Value, 'cert_dir', _('Certificate Directory'));
		o.rmempty = true;
		o.placeholder = '/etc/nginx/certs/luci-manager';

		return m.render().then(function(node) {
			/* ========== Read-only View Section ========== */
			var readonlySection = E('div', { 'class': 'cbi-section' });
			readonlySection.appendChild(E('h3', {}, _('Read-only View')));

			var readonlyBtns = E('div', { 'class': 'nm-btn-group', 'style': 'margin-bottom: 0.5em;' });

			readonlyBtns.appendChild(E('button', {
				'type': 'button',
				'class': 'cbi-button',
				'click': function() {
					callGetNginxT().then(function(result) {
						ui.showModal(_('nginx -T Output'), [
							E('pre', { 'class': 'nm-code-block' }, (result && result.content) || ''),
							E('div', { 'class': 'right' }, [
								E('button', { 'type': 'button', 'class': 'btn', 'click': function() { ui.hideModal(); } }, _('Close'))
							])
						]);
					});
				}
			}, _('View nginx -T')));

			readonlyBtns.appendChild(E('button', {
				'type': 'button',
				'class': 'cbi-button',
				'click': function() {
					callGetFileReadonly('/etc/nginx/uci.conf.template').then(function(result) {
						ui.showModal(_('/etc/nginx/uci.conf.template'), [
							E('pre', { 'class': 'nm-code-block' }, (result && result.content) || _('File not found')),
							E('div', { 'class': 'right' }, [
								E('button', { 'type': 'button', 'class': 'btn', 'click': function() { ui.hideModal(); } }, _('Close'))
							])
						]);
					});
				}
			}, _('View uci.conf.template')));

			readonlySection.appendChild(readonlyBtns);
			node.appendChild(readonlySection);

			/* ========== Danger Zone Section ========== */
			var dangerous = (uci.get('nginx_manager', 'global', 'dangerous_core_edit') || '0') === '1';

			var dangerZone = E('div', { 'class': 'cbi-section nm-danger-zone' });
			dangerZone.appendChild(E('h3', {}, _('Danger Zone')));

			var dangerWarning = E('div', { 'class': 'alert-message warning' });
			dangerWarning.appendChild(E('p', {}, _('Modifying core configuration may prevent Nginx from starting, which could make the LuCI web interface inaccessible. Please ensure SSH is available or keep uhttpd as a backup entry point.')));
			dangerZone.appendChild(dangerWarning);

			var dangerToggle = E('button', {
				'type': 'button',
				'class': 'cbi-button cbi-button-reset',
				'style': 'margin-bottom: 1em;',
				'click': function() {
					var button = this;
					var current = uci.get('nginx_manager', 'global', 'dangerous_core_edit') || '0';
					var newVal = current === '1' ? '0' : '1';
					button.disabled = true;
					return callSetDangerousCoreEdit(newVal).then(function(result) {
						if (result && result.error) {
							button.disabled = false;
							ui.addNotification(null, E('p', {}, _('Save failed') + ': ' + (result.detail || result.error)), 'error');
							return;
						}
						ui.addNotification(null, E('p', {}, newVal === '1' ? _('Dangerous edit mode enabled') : _('Dangerous edit mode disabled')), newVal === '1' ? 'warning' : 'info');
						setTimeout(function() { location.reload(); }, 500);
					}).catch(function(err) {
						button.disabled = false;
						ui.addNotification(null, E('p', {}, _('Save failed') + ': ' + (err.message || err)), 'error');
					});
				}
			}, dangerous ? _('Disable Dangerous Edit') : _('Enable Dangerous Edit'));

			dangerZone.appendChild(dangerToggle);

			if (dangerous) {
				var files = [
					{ path: '/etc/nginx/uci.conf.template', label: 'uci.conf.template' },
					{ path: '/etc/nginx/nginx.conf', label: 'nginx.conf' }
				];

				files.forEach(function(file) {
					var fileDiv = E('div', { 'class': 'nm-danger-file' });
					fileDiv.appendChild(E('h5', {}, file.label));

					var textarea = E('textarea', {
						'class': 'cbi-input-textarea nm-danger-editor',
						'id': 'editor-' + file.label.replace(/[^a-z]/g, '')
					});

					callGetFileReadonly(file.path).then(function(result) {
						if (result && result.content) {
							textarea.value = result.content;
						} else {
							textarea.value = _('File not found or not readable');
							textarea.disabled = true;
						}
					});

					fileDiv.appendChild(textarea);

					fileDiv.appendChild(E('button', {
						'type': 'button',
						'class': 'cbi-button cbi-button-reset',
						'click': function() {
							ui.showModal(_('Confirm Save'), [
								E('p', {}, _('Saving this file may break Nginx configuration. A backup will be created first. Continue?')),
								E('div', { 'class': 'right' }, [
									E('button', { 'type': 'button', 'class': 'btn', 'click': function() { ui.hideModal(); } }, _('Cancel')),
									E('button', {
										'type': 'button',
										'class': 'cbi-button cbi-button-reset',
										'click': function() {
											ui.hideModal();
											callSetFileDangerous(file.path, textarea.value).then(function(result) {
												if (result && result.error) {
													ui.addNotification(null, E('p', {}, _('Save failed') + ': ' + (result.detail || result.error)), 'error');
												} else {
													ui.addNotification(null, E('p', {}, _('File saved successfully')), 'info');
												}
											}).catch(function(err) {
												ui.addNotification(null, E('p', {}, _('Save failed') + ': ' + (err.message || err)), 'error');
											});
										}
									}, _('Save'))
								])
							]);
						}
					}, _('Save')));

					dangerZone.appendChild(fileDiv);
				});
			}

			node.appendChild(dangerZone);

			/* ========== Footer ========== */
			utils.appendFooter(node, {
				project: 'Nginx Manager',
				repoUrl: 'https://github.com/hello-yunshu/luci-app-nginx-manager'
			});

			return node;
		});
	},

	handleSave: function(ev, mode) {
		if (!this.validateBeforeSave())
			return Promise.resolve();

		return this.map.save(null, true).then(function() {
			ui.addNotification(null, E('p', {}, _('Configuration saved')), 'info');
		}).catch(function(err) {
			ui.addNotification(null, E('p', {}, _('Save failed') + ': ' + (err.message || err)), 'error');
		});
	},

	handleSaveApply: function(ev, mode) {
		var view = this;

		if (!this.validateBeforeSave())
			return Promise.resolve();

		return this.map.save(null, true).then(function() {
			var dangerous = (uci.get('nginx_manager', 'global', 'dangerous_core_edit') || '0') === '1';
			if (dangerous) {
				ui.addNotification(null, E('p', {}, _('Changes saved but not applied because dangerous edit mode is enabled. Disable it to apply changes.')), 'warning');
				return;
			}
			return utils.safeApply().then(function() {
				return callApplyCoreConfig();
			}).then(function(result) {
				if (result && result.error) {
					if (result.error === 'custom_ssl_directives_conflict')
						view.showCustomSslConflictModal(String(result.detail || '').split(/\n+/).filter(Boolean));
					else
						ui.addNotification(null, E('p', {}, _('Apply failed') + ': ' + (result.detail || result.error)), 'error');
				} else {
					ui.addNotification(null, E('p', {}, _('Core config saved and applied')), 'info');
				}
			});
		}).catch(function(err) {
			ui.addNotification(null, E('p', {}, _('Save failed') + ': ' + (err.message || err)), 'error');
		});
	},

	handleReset: function(ev, mode) {
		return this.map.reset().then(function() {
			ui.addNotification(null, E('p', {}, _('Configuration reset')), 'info');
		}).catch(function(err) {
			ui.addNotification(null, E('p', {}, _('Reset failed') + ': ' + (err.message || err)), 'error');
		});
	}
});
